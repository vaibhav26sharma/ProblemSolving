package org.problemsolving.linkedlist;

public class ListNode {
  int val;
  ListNode next;

  ListNode() {}

  ListNode(int x) {
    val = x;
  }

  ListNode(int val, ListNode next) {
    this.val = val;
    this.next = next;
  }
}

